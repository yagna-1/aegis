package scaffold

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"
)

type Options struct {
	Dir      string
	Services []string
	Port     int
	Force    bool
}

func Run(opts Options) error {
	if opts.Dir == "" {
		var err error
		opts.Dir, err = os.Getwd()
		if err != nil {
			return err
		}
	}
	if opts.Port == 0 {
		opts.Port = 8080
	}

	configPath := filepath.Join(opts.Dir, "aegis.yaml")
	envPath := filepath.Join(opts.Dir, ".env.example")

	if !opts.Force {
		if _, err := os.Stat(configPath); err == nil {
			return fmt.Errorf("aegis.yaml already exists (use --force to overwrite)")
		}
	}

	config := buildConfig(opts)
	env := buildEnv(opts)

	if err := os.WriteFile(configPath, []byte(config), 0o644); err != nil {
		return fmt.Errorf("writing aegis.yaml: %w", err)
	}
	if err := os.WriteFile(envPath, []byte(env), 0o644); err != nil {
		return fmt.Errorf("writing .env.example: %w", err)
	}

	fmt.Printf("✓ Created %s\n", configPath)
	fmt.Printf("✓ Created %s\n", envPath)
	fmt.Println()
	fmt.Println("Next steps:")
	fmt.Printf("  cp .env.example .env\n")
	fmt.Printf("  # Edit .env with your real secrets\n")
	fmt.Printf("  aegis          # start proxy on :%d\n", opts.Port)
	fmt.Printf("  aegis -mode mcp  # or start MCP server\n")
	fmt.Println()
	fmt.Println("Add to .gitignore:")
	fmt.Println("  .env")
	fmt.Println("  aegis.yaml")

	return nil
}

