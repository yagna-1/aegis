func buildConfig(opts Options) string {
	var sb strings.Builder

	sb.WriteString("# aegis.yaml — generated by `aegis init`\n")
	sb.WriteString("# Add .env and aegis.yaml to .gitignore\n\n")
	sb.WriteString(fmt.Sprintf("port: %d\n\n", opts.Port))
	sb.WriteString("# Optional: write structured NDJSON log to file\n")
	sb.WriteString("# audit_log: ~/.aegis/requests.ndjson\n\n")

	sb.WriteString("allowlist:\n")
	seen := map[string]bool{}
	for _, svc := range opts.Services {
		if s, ok := knownServices[svc]; ok {
			if !seen[s.allowlist] {
				sb.WriteString(s.allowlist + "\n")
				seen[s.allowlist] = true
			}
		} else {
			sb.WriteString(fmt.Sprintf("  # - api.%s.com\n", svc))
		}
	}
	if len(opts.Services) == 0 {
		sb.WriteString("  # - api.github.com\n")
		sb.WriteString("  # - api.stripe.com\n")
	}

	sb.WriteString("\n# Budget guards — prevent runaway agent API spend\n")
	sb.WriteString("# budgets:\n")
	sb.WriteString("#   api.github.com:\n")
	sb.WriteString("#     max_requests_per_hour: 200\n")
	sb.WriteString("#     loop_threshold: 5        # warn if same URL called 5x in 2 min\n\n")

	sb.WriteString("credentials:\n")
	for _, svc := range opts.Services {
		if s, ok := knownServices[svc]; ok {
			sb.WriteString(fmt.Sprintf("  %s:\n", s.domain))
			sb.WriteString(fmt.Sprintf("    header: %s\n", s.header))
			sb.WriteString(fmt.Sprintf("    template: \"%s\"\n\n", s.template))
		}
	}
	if len(opts.Services) == 0 {
		sb.WriteString("  # api.github.com:\n")
		sb.WriteString("  #   header: Authorization\n")
		sb.WriteString("  #   template: \"Bearer ${GITHUB_TOKEN}\"\n")
	}

	return sb.String()
}

func buildEnv(opts Options) string {
	var sb strings.Builder
	sb.WriteString("# .env.example — copy to .env and fill in real values\n")
	sb.WriteString("# NEVER commit .env to version control\n\n")
	for _, svc := range opts.Services {
		if s, ok := knownServices[svc]; ok {
			sb.WriteString(fmt.Sprintf("%s=your_%s_here\n", s.envVar, strings.ToLower(s.envVar)))
		}
	}
	if len(opts.Services) == 0 {
		sb.WriteString("# GITHUB_TOKEN=ghp_...\n")
		sb.WriteString("# STRIPE_SECRET_KEY=sk_live_...\n")
	}
	return sb.String()
}

